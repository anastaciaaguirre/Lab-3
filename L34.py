def kg_lb(kilo):
    lbs=2.2046244
    value=kilo*lbs
    print(kilo, "kilograms is", value, "pounds.")

def km_mi(kilo):
    mi=0.6213688
    value=kilo*mi
    print(kilo, "kilometers is", value, "miles.")


def m_ft(meter):
    ft=3.280839
    value=meter*ft
    print(meter, "meters is", value, "feet.")

